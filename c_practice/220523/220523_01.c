# include <stdio.h>

int main() {

		printf("%d\n", 10);
		printf("%f\n", 0.1f);
		printf("%c\n", 'a');
		printf("%s\n", "Hello, World!");

		const int con1 = 1;
		const float con2 = 0.1f;
		const char con3 = 'a';

		printf("%d\n", con1);
		printf("%f\n", con2);
		printf("%c\n", con3);


		return 0;
}
